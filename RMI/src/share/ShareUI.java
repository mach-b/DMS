    /**
     * When the 'Upload File' button is clicked the user is presented with a file
     * chooser dialog to select a file to upload to add to the shared files
     * 
     * @param evt the event that triggered the method
     */
    private void btnLoadFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoadFileActionPerformed
        
        int returnVal = FILE_CHOOSER.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            
            File file = FILE_CHOOSER.getSelectedFile();
            if (SHARED_FILES.addFile(file)) {
                
                System.out.println("File added:" + file.getName());
                updateFileListLocal(SHARED_FILES.getFileNames());
            } else {
                
                System.out.println("File not added");
            }
        }
    }//GEN-LAST:event_btnLoadFileActionPerformed

    /**
     * When the 'Download File' button is clicked the app will then download the
     * file to the RMIFileExchangeFolder
     * 
     * @param evt the event that triggered the method
     */
    private void btnSaveFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveFileActionPerformed
        
        String[] selection = getCurrentRemoteSelection();
        if (selection != null) {
            
            String ip = selection[0];
            String fileName = selection[1];
            File file = SHARED_FILES.getRemoteFile(ip, fileName);
            DirectoryManager.saveFile(file); 
            if (file == null) {

                downloadWarning("Failed to get remote files from host:\n\nHost not found");
            }
        }
    }//GEN-LAST:event_btnSaveFileActionPerformed

    /**
     * Display the current network snapshot of time stamps
     * 
     * @param evt the event that triggered the method
     */
    private void mnuSnapshotActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnuSnapshotActionPerformed
        
        HashMap<String, String> timeStamps = PEER_NODE.getTimeStamps();
        String snapshot = "System Snapshot\n";
        for (Map.Entry entry: timeStamps.entrySet()) {
            // Iterate through the map and add values to the result
            String ip = entry.getKey().toString().split(":")[0];
            snapshot += "\nIP:" + ip + " Vector:" + entry.getValue();
        }
        displayMessage("Snapshot", snapshot);
    }//GEN-LAST:event_mnuSnapshotActionPerformed

